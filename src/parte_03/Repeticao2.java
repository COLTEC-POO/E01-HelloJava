package parte_03;

public class Repeticao2 {

    public static void main(String[] args) {
        int soma = 0;

        // Loop de 100 a 1000
        for (int i = 100; i <= 1000; i++) {
            soma += i; // Adiciona o valor de i à soma
        }

        // Imprime a soma
        System.out.println("A soma dos números de 100 a 1000 é: " + soma);
    }


}
