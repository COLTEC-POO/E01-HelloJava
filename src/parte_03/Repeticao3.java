package parte_03;

public class Repeticao3 {

    public static void main(String[] args) {
        // Loop de 1 a 100
        for (int i = 1; i <= 100; i++) {
            // Verifica se o número é um múltiplo de 7
            if (i % 7 == 0) {
                System.out.println(i); // Imprime o número
            }
        }
    }

}
